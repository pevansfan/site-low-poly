function showOriginal() {
    let containerImg = document.querySelector(".container-img")
    let containerImg2 = document.querySelector(".container-img2")
    containerImg.style.display = 'none'
    containerImg2.style.display = 'flex'
}

function showLowPoly() {
    let containerImg = document.querySelector(".container-img")
    let containerImg2 = document.querySelector(".container-img2")
    containerImg.style.display = 'flex'
    containerImg2.style.display = 'none'
}